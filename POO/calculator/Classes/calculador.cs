
namespace calculator.Classes

    public class Calculadora
    
        public float numero1;

        public float numero2;

        public void Somar()
        {
            float resultado = numero1 + numero2;
        }

        public void Subtrair()
        {
            float resultado = numero1 - numero2;
        }

        public void Multiplicar()
        {
            float resultado = numero1 * numero2;
        }

        public void Dividir()
        {
            float resultado = numero1 / numero2;
        }